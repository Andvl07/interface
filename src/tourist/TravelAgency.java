package tourist;

/**
 * Интерфейс для представления туристической фирмы.
 */
interface TravelAgency {
    /**
     * Возвращает название туристической фирмы.
     * @return Название фирмы.
     */
    String getName();

    /**
     * Возвращает прибыль туристической фирмы за текущий год.
     * @return Прибыль за текущий год.
     */
    double getProfitForCurrentYear();
}

/**
 * Интерфейс для компаратора, сортирующего туристические фирмы по прибыли.
 */
interface ProfitComparator {
    /**
     * Сравнивает две туристические фирмы по их прибыли.
     * @param a Первая туристическая фирма.
     * @param b Вторая туристическая фирма.
     * @return Отрицательное число, если прибыль a < b, положительное - если a > b, 0 - если равны.
     */
    int compare(TravelAgency a, TravelAgency b);
}